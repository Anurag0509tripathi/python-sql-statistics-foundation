# Python operators practice

a = 20
b = 6

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** b)

print("Is a greater than b?", a > b)
print("Is a equal to b?", a == b)
print("Is a not equal to b?", a != b)
